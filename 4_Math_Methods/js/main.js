//Math Methods and Properties

console.log(Math.PI);
console.log(Math.trunc(Math.PI));
console.log(Math.round(3.4));
console.log(Math.round(3.5));
console.log(Math.ceil(Math.PI)); //It will round the value up
console.log(Math.floor(Math.PI)); //It will round the value down
console.log(Math.pow(2, -8));
console.log(Math.min(2, -8, 4));
console.log(Math.max(2, -8, 4));
console.log(Math.random()); //Always 0 to 1 and it includes 0 but not 1

//A random number from 1 to 10
console.log(Math.floor(Math.random() * 10) + 1 );
