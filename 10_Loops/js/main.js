// Loops

// while

let myNumber = 0;
while (myNumber <= 50) {
  console.log(myNumber);
  myNumber++;
}
// don't create an endless loop

//do while

let myNumber1 = 0;
do {
  myNumber1 += 2;
  console.log(myNumber1);
} while (myNumber1 < 100);

